    function actualizarIconoCarrito() {
        let carrito = [];
        try {
            const stored = JSON.parse(localStorage.getItem("carrito"));
            const now = new Date().getTime();

            // ✅ si existe data, úsala (aunque esté vencida)
            if (stored && Array.isArray(stored.data)) {
                // si quieres respetar expiración, ok; pero para UI es mejor no ocultar
                carrito = stored.data;

                // si está vencido, puedes limpiar aquí:
                if (stored.timestamp && now - stored.timestamp >= 2 * 60 * 60 * 1000) {
                    // opcional: limpiar si deseas
                    localStorage.removeItem("carrito");
                    carrito = [];
                }
            }
        } catch (e) {
            console.warn("carrito malformado o expirado");
        }

        const totalItems = carrito.reduce((sum, item) => sum + item.cantidad, 0);
        $(".contadorCarrito").text(totalItems);

        let totalValor = 0;
        let totalDescuento = 0;
        $(".listaProductoCarrito").empty(); // limpiar antes de agregar

        if (carrito.length != 0) {
            carrito.forEach((item, index) => {
                let addViewDescuento = "";
                let ahorroView = "";

                const cant = parseInt(item.cantidad, 10) || 0;
                const precioDesc = parseFloat(item.valor_descuento) || 0;
                const precioNormal = parseFloat(item.precio) || 0;

                const subtotal = precioDesc * cant;
                const subtotalNormal = precioNormal * cant;
                const ahorroUnitario = Math.max(0, precioNormal - precioDesc);

                totalValor += subtotal;

                if (parseFloat(item.descuento) != 0) {
                    const subtotalDes = subtotalNormal;
                    totalDescuento += subtotalDes;

                    const subtotalAntes = precioNormal * cant;
                    addViewDescuento = `
        <span class="cart-mini-old-price">
          ${formatoMoneda.format(subtotalAntes)}
        </span>`;
                    ahorroView = `
        <span class="cart-mini-saving-badge">
          Ahorras ${formatoMoneda.format(ahorroUnitario * cant)}
        </span>`;
                }

                $(".listaProductoCarrito").append(`
      <li data-id="${item.id}" class="cart-mini-item">
        <div class="d-flex align-items-start gap-2 w-100">

          <!-- Imagen -->
          <div class="cart-mini-thumb flex-shrink-0">
