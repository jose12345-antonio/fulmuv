              <button type="button" class="btn btn-outline-primary btn-sm qty-down-mini"
                data-id="${item.id}">−</button>

              <input type="number" min="1" step="1"
                class="form-control form-control-sm text-center input-qty-mini mini-qty"
                style="width:84px"
                data-id="${item.id}"
                value="${cant}" readonly />

              <button type="button" class="btn btn-outline-primary btn-sm qty-up-mini"
                data-id="${item.id}">+</button>
            </div>

            <!-- Precio x cant + subtotal -->
            <div class="text-end mt-2">
              <div class="text-muted small cart-mini-price-line">
