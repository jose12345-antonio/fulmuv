    function capitalizarPrimeraLetra(texto) {
        if (!texto) return ''; // Manejo de cadena vacía
        texto = texto.toLowerCase(); // Convierte todo a minúscula
        return texto.charAt(0).toUpperCase() + texto.slice(1);
    }

