        function rankSearchItems(items, term, type) {
            const terms = splitSearchTerms(term);
            const normalizedQuery = normalizeSearchText(term);

            if (!terms.length) {
                return Array.isArray(items) ? items : [];
            }

            return (Array.isArray(items) ? items : [])
                .map((item, index) => {
                    const haystack = normalizeSearchText(pickSearchableParts(item, type).join(" "));
                    if (!haystack) return null;

                    let matchedTerms = 0;
                    let titleMatches = 0;
                    const titleHaystack = normalizeSearchText([
                        item.titulo_producto,
                        item.nombre,
                        item.titulo,
                        item.marca_nombre,
                        item.modelo_nombre
                    ].filter(Boolean).join(" "));

                    terms.forEach(searchTerm => {
                        if (haystack.includes(searchTerm)) matchedTerms += 1;
                        if (titleHaystack.includes(searchTerm)) titleMatches += 1;
                    });

                    if (!matchedTerms) return null;

                    const exactPhrase = normalizedQuery && haystack.includes(normalizedQuery) ? 1 : 0;
                    const startsWithPhrase = normalizedQuery && titleHaystack.startsWith(normalizedQuery) ? 1 : 0;

                    return {
                        item,
                        index,
                        matchedTerms,
                        exactPhrase,
                        startsWithPhrase,
                        titleMatches
                    };
                })
                .filter(Boolean)
                .sort((a, b) => {
                    if (b.matchedTerms !== a.matchedTerms) return b.matchedTerms - a.matchedTerms;
                    if (b.exactPhrase !== a.exactPhrase) return b.exactPhrase - a.exactPhrase;
                    if (b.titleMatches !== a.titleMatches) return b.titleMatches - a.titleMatches;
                    if (b.startsWithPhrase !== a.startsWithPhrase) return b.startsWithPhrase - a.startsWithPhrase;
                    return a.index - b.index;
                })
                .map(entry => entry.item);
        }

        function mergeUniqueItemsById(groups, idKey) {
            const seen = new Set();
