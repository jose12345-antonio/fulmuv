        function performSearch(term) {
            const cleaned = String(term || "").trim();
            if (!cleaned) {
                lastResults = null;
                renderInitialState();
                return;
            }

            if (cache.has(cleaned)) {
                renderResults(cache.get(cleaned), cleaned);
                return;
            }

            if (activeRequest && typeof activeRequest.abort === "function") {
                activeRequest.abort();
            }

            renderLoadingState(cleaned);
            const reqGeneral = $.post("api/v1/fulmuv/productos/busqueda", {
                q: cleaned,
                categoria: "all"
            }, null, "json");
            const reqProducts = $.post("api/v1/fulmuv/ProductosSearch/All", {
                search: cleaned
            }, null, "json");
            const reqServices = $.post("api/v1/fulmuv/serviciosProductosSearch/All", {
                search: cleaned
            }, null, "json");

            activeRequest = {
                abort: function() {
                    reqGeneral.abort();
                    reqProducts.abort();
                    reqServices.abort();
                }
            };

            $.when(reqGeneral, reqProducts, reqServices)
                .done(function(generalResponse, productsResponse, servicesResponse) {
                    const generalRes = generalResponse && generalResponse[0] ? generalResponse[0] : {};
                    const productsRes = productsResponse && productsResponse[0] ? productsResponse[0] : {};
                    const servicesRes = servicesResponse && servicesResponse[0] ? servicesResponse[0] : {};

                    if (generalRes && generalRes.error) {
                        renderErrorState();
                        return;
                    }

                    const generalData = generalRes && generalRes.data ? generalRes.data : {};
                    const generalProducts = Array.isArray(generalData.products) ? generalData.products : [];
                    const apiProducts = Array.isArray(productsRes.data) ? productsRes.data : [];
                    const generalServices = Array.isArray(generalData.services) ? generalData.services : [];
                    const apiServices = Array.isArray(servicesRes.data) ? servicesRes.data : [];
                    const mergedData = {
                        products: rankSearchItems(mergeUniqueItemsById([generalProducts, apiProducts], "id_producto"), cleaned, "products"),
                        services: rankSearchItems(mergeUniqueItemsById([generalServices, apiServices], "id_producto"), cleaned, "services"),
                        vehicles: rankSearchItems(Array.isArray(generalData.vehicles) ? generalData.vehicles : [], cleaned, "vehicles"),
                        events: rankSearchItems(Array.isArray(generalData.events) ? generalData.events : [], cleaned, "events"),
                        jobs: rankSearchItems(Array.isArray(generalData.jobs) ? generalData.jobs : [], cleaned, "jobs")
                    };

                    cache.set(cleaned, mergedData);
                    try {
                        sessionStorage.setItem('fulmuvSmartSearch:' + cleaned.toLowerCase(), JSON.stringify(mergedData));
                    } catch (e) {}
                    renderResults(mergedData, cleaned);
                })
                .fail(function(xhr, status) {
                    const requestStatus = status || (xhr && xhr.statusText) || "";
                    if (requestStatus === "abort") return;
                    renderErrorState();
