        function stripHtml(value) {
            return String(value ?? "")
                .replace(/<[^>]*>/g, " ")
                .replace(/&nbsp;/gi, " ")
                .replace(/\s+/g, " ")
                .trim();
        }

        function normalizeSearchText(value) {
            return stripHtml(value)
